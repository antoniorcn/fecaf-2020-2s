package edu.curso;

public class ExemploLacoWhile {
	public static void main(String[] args) {
		System.out.println("Inicio do Programa");
		int i = 1;				// Inicialização
		while (i <= 3) {		// Condição
			System.out.println("HiHappy - Toys");
			
			i = i + 1;			// Incremento
		}
		System.out.println("Fim do Programa");

	}
}
