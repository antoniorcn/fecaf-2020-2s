package gti.curso;

public class Exercicio3 {
	public static void main(String[] args) { 
		/* 
		 * Crie um programa que mostre na tela 
		 * a seguinte expressão e seu resultado: 
		 * 4 * 8 / 2 = 16
		*/
		float a = 4.0f;		// Guarda o primeiro termo
		float b = 8.0f;
		float c = 2.0f;
		float res = a * b / c;
		
		// System.out.println("4 * 8 / 2 = 16");
		System.out.println(a + " * " + b + " / " + c + " = " + res);
	}
}
